package com.vilniuscoding.hotel;


public class Booking {
	
	int id;
	int bookDate;
	int bookStart;
	int bookEnd;
	int totalPay;
	String coments;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getBookDate() {
		return bookDate;
	}
	public void setBookDate(int bookDate) {
		this.bookDate = bookDate;
	}
	public int getBookStart() {
		return bookStart;
	}
	public void setBookStart(int bookStart) {
		this.bookStart = bookStart;
	}
	public int getBookEnd() {
		return bookEnd;
	}
	public void setBookEnd(int bookEnd) {
		this.bookEnd = bookEnd;
	}
	public int getTotalPay() {
		return totalPay;
	}
	public void setTotalPay(int totalPay) {
		this.totalPay = totalPay;
	}
	public String getComents() {
		return coments;
	}
	public void setComents(String coments) {
		this.coments = coments;
	}
	
	
	

}
